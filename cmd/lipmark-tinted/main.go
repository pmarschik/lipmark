func generatePaletteFields(w io.Writer, s *scheme) {
	for _, f := range paletteFieldMap {
		color, ok := s.Palette[f.key]
		if !ok {
			continue
		}
		fmt.Fprintf(w, "\t%s: theme.StyleDef{Foreground: %q},\n", f.field, color)
	}
}

func renderAttrs(bold, faint, italic, uline bool) string {
	var parts []string
	if bold {
		parts = append(parts, "Bold: true")
	}
	if faint {
		parts = append(parts, "Faint: true")
	}
	if italic {
		parts = append(parts, "Italic: true")
	}
	if uline {
		parts = append(parts, "Underline: true")
	}
	if len(parts) == 0 {
		return ""
	}
	return ", " + strings.Join(parts, ", ")
}

func schemeToVar(name string, export bool) string {
	// Convert "catppuccin-mocha" to "CatppuccinMocha" (exported) or "catppuccinMocha" (unexported).
	parts := strings.Split(name, "-")
	for i, p := range parts {
		if p != "" {
			if i == 0 && !export {
				parts[i] = strings.ToLower(p[:1]) + p[1:]
			} else {
				parts[i] = strings.ToUpper(p[:1]) + p[1:]
			}
		}
	}
	return strings.Join(parts, "")
}

func fetchSchemeData(name string) (*scheme, error) {
	safe, err := sanitizeName(name)
	if err != nil {
		return nil, err
	}
	filename := safe + ".yaml"
	for _, baseURL := range []string{base24URL, base16URL} {
		resp, httpErr := http.Get(baseURL + filename) //nolint:gosec,noctx // CLI tool
		if httpErr != nil {
			continue
		}
		if resp.StatusCode != http.StatusOK {
			resp.Body.Close()
			continue
		}
		var s scheme
		decErr := yaml.NewDecoder(resp.Body).Decode(&s)
		resp.Body.Close()
		if decErr != nil {
			return nil, fmt.Errorf("decode %s: %w", name, decErr)
		}
		fmt.Fprintf(os.Stderr, "Fetched %s (%s, %s)\n", name, s.System, s.Variant)
		return &s, nil
	}
	return nil, fmt.Errorf("scheme %q not found", safe)
}

// cmdFetch downloads a raw YAML scheme file (for apps that want runtime loading).
func cmdFetch(args []string) error {
	if len(args) < 1 {
		return errors.New("fetch requires a scheme name")
	}
	name := args[0]
	dir := "."
	for i := 1; i < len(args); i++ {
		if args[i] == "-o" && i+1 < len(args) {
			dir = args[i+1]
			i++
		}
	}
	return fetchYAML(name, dir)
}

func fetchYAML(name, dir string) error {
	safe, err := sanitizeName(name)
	if err != nil {
		return err
	}
	filename := safe + ".yaml"
	for _, baseURL := range []string{base24URL, base16URL} {
		resp, httpErr := http.Get(baseURL + filename) //nolint:gosec,noctx // CLI tool
		if httpErr != nil {
			continue
		}
		if resp.StatusCode != http.StatusOK {
			resp.Body.Close()
			continue
		}
		outPath := filepath.Join(dir, filename)
		f, createErr := os.Create(outPath) //nolint:gosec // sanitized
		if createErr != nil {
			resp.Body.Close()
			return fmt.Errorf("create %s: %w", outPath, createErr)
		}
		_, copyErr := io.Copy(f, resp.Body)
		f.Close()
		resp.Body.Close()
		if copyErr != nil {
			return fmt.Errorf("write %s: %w", outPath, copyErr)
		}
		fmt.Fprintf(os.Stderr, "Downloaded %s to %s\n", safe, outPath)
		return nil
	}
	return fmt.Errorf("scheme %q not found", safe)
}

func sanitizeName(name string) (string, error) {
	clean := filepath.Base(name)
	if clean != name || strings.ContainsAny(name, `/\`) || name == "." || name == ".." {
		return "", fmt.Errorf("invalid scheme name %q", name)
	}
	return clean, nil
}
